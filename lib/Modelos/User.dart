class User {
  String nome;
  String email;
  String celular;
  String endereco;
  String cep;
  String cpf;
  String senha;

  User(String nome, String email, String celular, String endereco, String cep, String cpf, String senha) {
    this.nome = nome;
    this.email = email;
    this.celular = celular;
    this.endereco = endereco;
    this.cep = cep;
    this.cpf = cpf;
    this.senha = senha;
  }
}